-- 11. Which category generated the highest revenue?
SELECT iti.category_code, iti.category_name, ROUND(SUM(its.quantity_sold * its.unit_selling_price), 0) AS revenue
FROM item_sales its
JOIN item_info iti ON its.item_code = iti.item_code
