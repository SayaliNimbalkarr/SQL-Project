-- 1. How many records are there in the sales table?
SELECT COUNT(*) FROM item_sales;

-- 2. How many unique products have been sold?
SELECT COUNT(DISTINCT item_code)
FROM item_sales
WHERE sale_or_return = 'sale';

-- 3. How many sales transactions and return transactions are there?
SELECT sale_or_return, COUNT(sale_or_return) FROM item_sales
GROUP BY sale_or_return;

-- 4. Which products belong to a specific category?
SELECT category_name, item_name, item_code
FROM item_info
ORDER BY 1, 2;

-- 5. Which 10 products were sold the most?
SELECT its.item_code, iti.item_name, COUNT(its.item_code) AS most_sold
FROM item_sales its
JOIN item_info iti ON its.item_code = iti.item_code
WHERE its.sale_or_return = 'sale'
GROUP BY 1, 2
ORDER BY 3 DESC
LIMIT 10;

-- 6. Which category contains the highest number of products?
SELECT category_name, category_code, COUNT(item_code) 
FROM item_info
GROUP BY 1, 2
ORDER BY 3 DESC
LIMIT 1;

-- 7. How many transactions received a discount?
SELECT COUNT(sale_or_return) 
FROM item_sales
WHERE discount = 'Yes';

-- 8. What is the total revenue generated by each product?
SELECT its.item_code, iti.item_name, ROUND(SUM(its.quantity_sold * its.unit_selling_price), 0) AS revenue
FROM item_sales its
JOIN item_info iti ON its.item_code = iti.item_code
WHERE its.sale_or_return = 'sale'
GROUP BY 1, 2
ORDER BY 3 DESC;

-- 9. What is the total revenue generated by each category?
SELECT iti.category_code, iti.category_name, ROUND(SUM(its.quantity_sold * its.unit_selling_price), 0) AS revenue
FROM item_sales its
JOIN item_info iti ON its.item_code = iti.item_code
WHERE its.sale_or_return = 'sale'
GROUP BY 1, 2
ORDER BY 3 DESC;

-- 10. Which 10 products generated the highest revenue?
